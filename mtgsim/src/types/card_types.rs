impl LandType {
    /// Whether this is one of the five basic land types (rule 305.6)
    pub fn is_basic_land_type(&self) -> bool {
        matches!(self, LandType::Plains | LandType::Island | LandType::Swamp | LandType::Mountain | LandType::Forest)
    }
}

